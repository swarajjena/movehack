class RakeAllocation extends React.Component {
  state = {
    order: 'asc',
    orderBy: 'preBerthTime',
    selected: [],
    data: [],
    page: 0,
    rowsPerPage: 10,
    dropdownValue: "11018",
    totalData: []
  };

  componentDidMount = () => {
    const hostAddress = host.Address || '104.211.96.209:4000';
    let data;
    axios.get(`http://${hostAddress}/api/rake_allocation_news`)
      .then(response => {
        console.log(response.data);
        data = response.data.filter(dataItem => dataItem.train_no == this.state.dropdownValue);
        this.setState({ totalData: response.data, data });
      })
      .catch(error => {
        console.log(error);
      });
  }

  handleRequestSort = (event, property) => {
    const orderBy = property;
    let order = 'desc';

    if (this.state.orderBy === property && this.state.order === 'desc') {
      order = 'asc';
    }

    this.setState({ order, orderBy });
  };

  handleSelectAllClick = (event, checked) => {
    if (checked) {
      this.setState(state => ({ selected: state.data.map(n => n.id) }));
      return;
    }
    this.setState({ selected: [] });
  };

  handleClick = (event, id) => {
    const { selected } = this.state;
    const selectedIndex = selected.indexOf(id);
    let newSelected = [];

    if (selectedIndex === -1) {
      newSelected = newSelected.concat(selected, id);
    } else if (selectedIndex === 0) {
      newSelected = newSelected.concat(selected.slice(1));
    } else if (selectedIndex === selected.length - 1) {
      newSelected = newSelected.concat(selected.slice(0, -1));
    } else if (selectedIndex > 0) {
      newSelected = newSelected.concat(
        selected.slice(0, selectedIndex),
        selected.slice(selectedIndex + 1),
      );
    }

    this.setState({ selected: newSelected });
  };

  handleChangePage = (event, page) => {
    this.setState({ page });
  };

  handleChangeRowsPerPage = event => {
    this.setState({ rowsPerPage: event.target.value });
  };

  isSelected = id => this.state.selected.indexOf(id) !== -1;

  dropdownChange = event => {
    const data = this.state.totalData.filter(dataItem => dataItem.train_no == event.target.value);
    this.setState({ dropdownValue: event.target.value, data });
  }

  render() {
    const { classes } = this.props;
    const { data, order, orderBy, selected, rowsPerPage, page } = this.state;
    const emptyRows = rowsPerPage - Math.min(rowsPerPage, data.length - page * rowsPerPage);

    return (
      <div>
        <select className={classes.filterdropdown} onChange={this.dropdownChange} value={this.state.dropdownValue}>
          <option value="11018">Train No 11018</option>
          <option value="11056">Train No 11056</option>
          <option value="11040">Train No 11040</option>
          <option value="11003">Train No 11003</option>
        </select>
        <Paper className={classes.root}>
          <EnhancedTableToolbar numSelected={selected.length} />
          <div className={classes.tableWrapper}>
            <Table className={classes.table} aria-labelledby="tableTitle">
              <EnhancedTableHead
                numSelected={selected.length}
                order={order}
                orderBy={orderBy}
                onSelectAllClick={this.handleSelectAllClick}
                onRequestSort={this.handleRequestSort}
                rowCount={data.length}
              />
              <TableBody>
                {data
                  .sort(getSorting(order, orderBy))
                  .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
                  .map((n, index) => {
                    const isSelected = this.isSelected(n.id);
                    return (
                      <TableRow
                        hover
                        onClick={event => this.handleClick(event, n.id)}
                        role="checkbox"
                        aria-checked={isSelected}
                        tabIndex={-1}
                        key={index}
                        selected={isSelected}
                      >
                        <TableCell component="th" scope="row">
                          {n.rake_no}
                        </TableCell>
                        <TableCell numeric>{n.container_no}</TableCell>
                        <TableCell numeric>{n.consignment_id}</TableCell>
                        <TableCell numeric>{n.vessle_no}</TableCell>
                        <TableCell numeric>{n.weight}</TableCell>
                        <TableCell numeric>{n.size}</TableCell>
                        <TableCell numeric>{n.loading_time}</TableCell>
                        <TableCell numeric>{n.container_out_time}</TableCell>
                        <TableCell numeric>{n.destination}</TableCell>
                        <TableCell numeric>{n.distance_tobe_travelled}</TableCell>
                      </TableRow>
                    );
                  })}
                {emptyRows > 0 && (
                  <TableRow style={{ height: 49 * emptyRows }}>
                    <TableCell colSpan={6} />
                  </TableRow>
                )}
              </TableBody>
            </Table>
          </div>
          <TablePagination
            component="div"
            count={data.length}
            rowsPerPage={rowsPerPage}
            page={page}
            backIconButtonProps={{
              'aria-label': 'Previous Page',
            }}
            nextIconButtonProps={{
              'aria-label': 'Next Page',
            }}
            onChangePage={this.handleChangePage}
            onChangeRowsPerPage={this.handleChangeRowsPerPage}
          />
        </Paper>
      </div>
    );
  }
}

RakeAllocation.propTypes = {
  classes: PropTypes.object.isRequired,
};

export default withStyles(styles)(RakeAllocation);
